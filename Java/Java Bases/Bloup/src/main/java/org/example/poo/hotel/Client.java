package org.example.poo.hotel;

public class Client {

    int id;
    String lastname;
    String firstname;
    int telephone;

    public Client(){
    }

    public Client(int id, String lastname, String firstname, int telephone) {
        this.id = id;
        this.lastname = lastname;
        this.firstname = firstname;
        this.telephone = telephone;
    }
}
